class Welcome extends React.Component{
    async componentDidMount (){
        this.getReady()
    }
    async getReady(){
        //decode is the page search query /home/?page=1&limit=2
        const decode = decodeURI(window.location.search).replace('?', '').split('&')
        .map(param => param.split('='))
        .reduce((values, [ key, value ]) => {
                values[ key ] = value
                return values
            }, {})

        //this is to go to the API and get all the data
        //I can pass a page and a limit to this function(default is page 1 and Limit 20) 
        if(decode.page){
        await this.props.loadAllMusic(decode.page)
        }else{
        await this.props.loadAllMusic()
        }
    }
    componentDidUpdate(){
        this.getReady()
    }
    render() {
        return (
        <div className="container">
            <div className="row text-center">
                <div className="col">
                    <h1 className="display-4">PX Music</h1>
                    <p>Tu sitio de descargas y noticias del genero</p>               
                </div>
            </div>
            <div className="alert alert-info py-2">

            <p className="mt-2">Lo Mas Reciente
                <br/>
                
            </p>
            </div>
            {loadandRenderMusic(_.values(this.props.music.docs))}
            {loadandRenderMusic(FakeArray)}
            <div>
            {pagination(_.values(this.props.music.docs))}
            </div>
        </div>
        )
    }
}

function mapStateToProps(state){
    return {
        music: state.music
    }
}
export default connect(mapStateToProps, actions)(Welcome)